package CBFCursos;
// class Math
public class Aula52 {
    public static void main(String[] args) {
      int min = Math.min(3, 5); // retorna o valor minimo
      int max = Math.max(6,10); // retorna o valor maximo
      System.out.printf("Valor maximo: %d \n Valor minimo: %d \n",max , min);

      System.out.println(" Raiz de 16 =" +Math.sqrt(16));; // retorna a raiz
      System.out.println("Mod:" + Math.abs(-5)); // Retorna valores positivos(modulo)

      System.out.println(Math.random()*11); // numeros aleatorio entra 0 e 1
    }
}
